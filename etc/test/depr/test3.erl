run() ->

	erlunit:strong_banner("Erlvolt Test Program #3 - Callback Architecture"),
	erlunit:start([colors]),

    %%%------------------------------------------------------------------------
	%%% 
	%%%------------------------------------------------------------------------
	
	erlunit:suite("Callback"),

	erlvolt:create_callback_table(),
	
	Fun = fun(Arg) -> Arg end,
	Id = erlvolt:add_callback(Fun),

	?ERLUNIT_SEQ_EQUAL(erlvolt:get_callback(Id), fun() -> Fun end),
	?ERLUNIT_SEQ_TRUE (erlvolt:get_callback(Id) == Fun),
	?ERLUNIT_SEQ_EQUAL(erlvolt:execute_callback(Id, ok_param), ok_param),
	?ERLUNIT_SEQ_EQUAL(erlvolt:resolve_callback(Id, ok_param), ok_param),
	?ERLUNIT_SEQ_EQUAL(erlvolt:get_callback_or_nil(something), nil),
	?ERLUNIT_SEQ_EQUAL(erlvolt:get_callback_or_nil(Id), nil),
	?ERLUNIT_SEQ_ERROR(erlvolt:get_callback(something)),
	?ERLUNIT_SEQ_ERROR(erlvolt:get_callback(Id)),
	?ERLUNIT_SEQ_FAIL (erlvolt:execute_callback(Id, any_param)),
	?ERLUNIT_SEQ_FAIL (erlvolt:resolve_callback(Id, any_param)),

	?ERLUNIT_SEQ_PASS (create_many_callbacks(10000, Fun)),
	?ERLUNIT_SEQ_PASS (create_many_callbacks2(10000)),
	?ERLUNIT_SEQ_PASS (create_many_callbacks3(1000)),
	?ERLUNIT_SEQ_PASS (create_many_callbacks4(1000)),

	%%%------------------------------------------------------------------------
	%%% Mounting of Tests Done. Execute.
	%%%------------------------------------------------------------------------

	erlunit:execute().

%%%------------------------------------------------------------------------
%%% Helper: Manyfold Callbacks
%%%------------------------------------------------------------------------
%%% Add to list only

create_many_callbacks(Count, Fun) ->

	statistics(runtime),
	
	create_callbacks(Count, Fun),
	
	{_, RunTime} = statistics(runtime),
	
	erlunit:echo("Created ~w callbacks in ~w ms", [Count, RunTime]),
	Count.


create_callbacks(Count, Fun) ->

	if
		Count > 0 ->
			erlvolt:add_callback(Fun),
			create_callbacks(Count-1, Fun);
		true -> nil
	end.


%%%------------------------------------------------------------------------
%%% Helper: Manyfold Callbacks 2
%%%------------------------------------------------------------------------
%%% Add and delete from list 

create_many_callbacks2(Count) ->

	Control = ets:new(callback_control_table, [ set, public, named_table, { keypos, 1 }, { write_concurrency, true  } ] ),

	statistics(runtime),

	ok = create_callbacks2(Control, Count),

	{_, CreateTime} = statistics(runtime),

	erlunit:echo("Created ~w callbacks with control table in ~w ms", [Count, CreateTime]),

	statistics(runtime),

	ok = delete_callbacks2(Control, Count),

	{_, DeleteTime} = statistics(runtime),

	erlunit:echo("Deleted ~w callbacks with control table in ~w ms", [Count, DeleteTime]),


	Count.


create_callbacks2(Control, Count) ->

	if
		Count > 0 ->
			Fun = fun(_Arg) -> Count end,
			Id = erlvolt:add_callback(Fun),
			true = ets:insert_new(Control, {Id,Fun}),
			create_callbacks2(Control, Count-1);
		true -> ok
	end.

delete_callbacks2(Control, Count) ->

	if
		Count > 0 ->
			Id = ets:first(Control),
			ets:delete(Control, Id),
			erlvolt:delete_callback(Id),
			delete_callbacks2(Control, Count-1);
		true -> ok
	end.


%%%------------------------------------------------------------------------
%%% Helper: Manyfold Callbacks 3
%%%------------------------------------------------------------------------
%%% Add straight and then pseudo randomly delete from list 

create_many_callbacks3(Count) ->

	statistics(runtime),

	Control = create_callbacks3([], Count),

	{_, CreateTime} = statistics(runtime),

	erlunit:echo("Created ~w callbacks with control list in ~w ms", [Count, CreateTime]),

	statistics(runtime),

	ok = delete_callbacks3(Control, Count, 0, 1 ),

	{_, DeleteTime} = statistics(runtime),

	erlunit:echo("Deleted ~w callbacks with control list in pseudo random order in ~w ms", [Count, DeleteTime]),

	Count.

create_callbacks3(Control, Count) ->

	if
		Count > 0 ->
			Fun = fun(_Arg) -> Count end,
			Id = erlvolt:add_callback(Fun),
			create_callbacks3([ {Id,Fun} | Control ], Count-1);
		true -> Control
	end.

delete_callbacks3(Control, Count, Seed1, Seed2) ->

	Count = length(Control), % a test

	if
		Count > 0 ->
			{ NewSeed, Index } = target_callback3_index(Count, Seed1, Seed2),
			{ Id, _ } = Entry  = lists:nth(Index, Control),
			% debug (crashes main loop) io:format(" ~w ", [ Index ]),
			ControlRest = lists:delete(Entry, Control),
			erlvolt:delete_callback(Id),
			delete_callbacks3(ControlRest, Count-1, NewSeed, Index);
		true -> 
			ok
	end.
	
% mildly pseudo random sequence, see (*) below
target_callback3_index(Max, Pre1, Pre2) ->

	R1 = (Pre1 + Pre2) rem Max + 1,
	if 
		R1 < Max div 10 -> { Pre2 + R1 -1, 1 };
		R1 < Max div 5 -> { Pre2 + R1 - Max, Max };
		true -> { Pre2, R1 }
	end.

	

%%%------------------------------------------------------------------------
%%% Helper: Manyfold Callbacks 4
%%%------------------------------------------------------------------------
%%% Add straight, then pseudo randomly delete from list while adding more

create_many_callbacks4(Initial) ->

	ReAdds = Initial div 4,

	statistics(runtime),

	Control = create_callbacks4([], Initial),

	{_, CreateTime} = statistics(runtime),

	erlunit:echo("Created ~w callbacks with control list in ~w ms", [Initial, CreateTime]),

	statistics(runtime),

	ok = delete_callbacks4_and_add_more(Control, 0, 1, ReAdds),

	{_, DeleteTime} = statistics(runtime),

	erlunit:echo("Deleted ~w callbacks with control list in pseudo random order and adding ~w on the way in ~w ms",
		[Initial, ReAdds, DeleteTime]),

	Initial.

create_callbacks4(Control, Count) ->

	if
		Count > 0 ->
			Fun = fun(_Arg) -> Count end,
			Id = erlvolt:add_callback(Fun),
			create_callbacks4([ {Id,Fun} | Control ], Count-1);
		true -> Control
	end.

delete_callbacks4_and_add_more(Control, Seed1, Seed2, ReAddsStill) ->

	Count = length(Control), % a test

	if
		Count > 0 ->
			{ NewSeed, Index } = target_callback4_index(Count, Seed1, Seed2),
			
			% Re-Add on average every 3rd (see (**) 'readd', below)
			{ NowControl, ReAddsLeft } = 
			
				case ((ReAddsStill > 0) and ((Index rem 3) == 0)) of
					true ->  
						% debug (crashes main loop): io:format(" (re-add to ~w) ", [Count+1]), 
						{ create_callbacks4(Control, 1), ReAddsStill - 1 };
					_ -> { Control, ReAddsStill }
				end,
			
			{ Id, _ } = Entry = lists:nth(Index, NowControl),
			% debug (crashes main loop): io:format(" ~w ", [ Index ]),
			ControlRest = lists:delete(Entry, NowControl),
			erlvolt:delete_callback(Id),
			delete_callbacks4_and_add_more(ControlRest, NewSeed, Index, ReAddsLeft);
		true -> 
			ok
	end.
	
% mildly pseudo random sequence, see (**) below
target_callback4_index(Max, Pre1, Pre2) ->

	{ Pre2, (Pre1 + Pre2) rem Max + 1 }.	
	
	
